package hbBasaleGenerator;

public class CSGBeam {
    String beam;
    String near_look_angle;
    String far_look_angle;
    String sensor_mode;

    public String getBeam() {
        return beam;
    }

    public void setBeam(String beam) {
        this.beam = beam;
    }

    public String getNear_look_angle() {
        return near_look_angle;
    }

    public void setNear_look_angle(String near_look_angle) {
        this.near_look_angle = near_look_angle;
    }

    public String getFar_look_angle() {
        return far_look_angle;
    }

    public void setFar_look_angle(String far_look_angle) {
        this.far_look_angle = far_look_angle;
    }

    public String getSensor_mode() {
        return sensor_mode;
    }

    public void setSensor_mode(String sensor_mode) {
        this.sensor_mode = sensor_mode;
    }
}
